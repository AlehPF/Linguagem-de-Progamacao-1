/*   
1. Definição de algoritmo:
R: Algoritmo é como uma sequência finita de ações executáveis bem definidas, com o
objetivo de resolver um problema ou executar uma tarefa. Um algoritmo, portanto,
conta com a entrada e saída de informações mediadas pelas instruções.
Os algoritmos podem ser como uma receita de bolo: uma sequência de ações que
devem ser executadas até que o objetivo final seja atingido, no caso da receita de bolo
seria o bolo pronto.

2. Variável: 
R: É um espaço identificado por um nome na memória do computador, para uso durante o programa. 
Ela pode ser do tipo int e números inteiros, do tipo float para números reais
e do tipo char para armazenar palavras ou caracteres.

3. Operadores de atribuição e comparação:
R: A atribuição é declarar um valor desejado para uma variável, logo usada pelo sinal "=". Já os operadores 
de comparação, podem ser "==", para verificar variável é igual a um valor ou caracterer, e o operador "!=" serve para verificar 
se a variável é diferente de um valor ou caracterer.

4. Comandos de seleção e repetição
R: Os comandos de seleção e repetição mais conhecidos são o: For, While, Do While e o Switch. O For, While e Do While servem para repitir 
determinado código enquanto a condição sejá verdadeira, já o comando Switch serve para selecionar determinada ação dependendo do valor
informado.

Fluxograma:
(==) : Inicio e fim;                                                (=Inicio=)
                                                                  
| --- |: Calculo e atribuição de valores;                           /=n1,n2=/ 
| --- |: Retangulo                                               |-som = n1 + n2-|

<=) :  mostra na tela;                                               |- soma-|
.                                                                        ~
/==/ : Entrada de dados;
.                                                                     (=Fim=)
\=\ : Armazenar os dados;

|--| : Saída de dados
 ~   : Saída de dados

Losango (Dois triangulos) : Tomada de decisao
<> (de cabeça para baixo)
*/